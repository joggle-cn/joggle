package com.wuweibi.bullet.domain.params;



/**
 * 
 * 登录参数
 * 
 * @author marker
 * @version 1.0
 */
public class LoginParam {

	/** 用户名/Email */
	private String name;
	
	/** 密码 */
	private String pass;

	
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
}
